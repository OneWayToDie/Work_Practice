//========================================================= Пространство имён ================================================================//
namespace Work_Practice.Models
{
	//========================================================= Модель результата анализа числа ================================================================//
	public class NumberInfo
	{
		//========================================================= Свойства ================================================================//
		public long Number { get; set; }      // Исходное число (тип long для поддержки больших значений)
		public int DigitCount { get; set; }   // Количество цифр в числе
		public int? MinDigit { get; set; }    // Наименьшая цифра в числе (nullable, т.к. для 0 или отрицательных может быть null)
	}
}
