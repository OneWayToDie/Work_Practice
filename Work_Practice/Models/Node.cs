//========================================================= Библиотеки ================================================================//
// Пространство имён без using – Node<T> не использует внешние зависимости.

namespace Work_Practice.Models
{
	//========================================================= Класс узла односвязного списка (обобщённый) ================================================================//
	public class Node<T>
	{
		//========================================================= Поля и свойства ================================================================//
		public T Data { get; set; }        // Данные, хранящиеся в узле
		public Node<T> Next { get; set; }  // Ссылка на следующий узел (или null, если узел последний)

		//========================================================= Конструктор ================================================================//
		public Node(T data)
		{
			Data = data;                   // Инициализируем данные
			Next = null;                   // При создании узла следующий узел отсутствует
		}
	}
}
